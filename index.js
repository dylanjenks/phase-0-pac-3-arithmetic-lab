function add(x, y) {
    return x + y;
  } 

function subtract(x, y) {
    return x - y;
  } 

function multiply(x, y) {
    return x * y;
  } 

function divide(x, y) {
    return x / y;
  } 

function increment(a) {
    return ++a;
  } 

function decrement(a) {
    return --a;
  } 

function makeInt(a) {
    return parseInt(a, 10)
}

function preserveDecimal(a) {
    return parseFloat(a)
}